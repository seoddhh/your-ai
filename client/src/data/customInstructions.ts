export function getInstructionById(id: string): CustomInstruction | undefined {
        return customInstructions.find(i => i.id === id);
}

export function getPopularInstructions(limit: number = 5): CustomInstruction[] {
        return [...customInstructions]
                .sort((a, b) => b.popularity - a.popularity)
                .slice(0, limit);
}

export function searchInstructions(query: string): CustomInstruction[] {
        const q = query.toLowerCase();
        return customInstructions.filter(i =>
                i.name.toLowerCase().includes(q) ||
                i.description.toLowerCase().includes(q) ||
                i.tags.some(t => t.toLowerCase().includes(q))
        );
}
